     * made from the product of two 2-digit numbers is 9009 = 91 Ã— 99.
     * Find the largest palindrome made from the product of two 3-digit numbers.
     */
    public function problem_4()
    {
        for($i = 999; $i > 800; $i--) {
            for($j = 999; $j > 800; $j--) {
                $p = $i * $j;

                if(substr($p,0,3) == strrev(substr($p,-3))) {
                    $solution[] = $p;
                }
            }
        }

        sort($solution);
        return end($solution);
    }

